package org.msh.etbm.commons.forms.controls;

/**
 * Describe the size of a control, using Twitter Bootstrap column sizes
 *
 * Created by rmemoria on 25/7/16.
 */
public class Size {

    private Integer xs;
    private Integer sm;
    private Integer md;
    private Integer lg;

    public Integer getXs() {
        return xs;
    }

    public void setXs(Integer xs) {
        this.xs = xs;
    }

    public Integer getSm() {
        return sm;
    }

    public void setSm(Integer sm) {
        this.sm = sm;
    }

    public Integer getMd() {
        return md;
    }

    public void setMd(Integer md) {
        this.md = md;
    }

    public Integer getLg() {
        return lg;
    }

    public void setLg(Integer lg) {
        this.lg = lg;
    }
}
